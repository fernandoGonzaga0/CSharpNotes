/*

LINQ (Language Integrated Query) -> recurso que permite escrever consultas diretamente na linguagem, de forma tiada e integrada, para manipular coleções de dados como listas, arrays, XML ou banco de dados.

    Principais métodos de consultas LINQ

    Any() - determina se qualquer elemento de uma sequência existe ou atende a uma condição.

    FirstOrDefault() - retorna o primeiro elemento da coleção que satisfaz uma condição opcional. Retorna o valor padrão do tipo caso não encontre nenhum elemento. 

    OrderBy() - Classifica os elementos na coleção em ordem crescente com base em uma determinada condicão e retorna a coleção classificada. 

    ToList() - recebe um tipo IEnumerable e o converte em um tipo List.

    Where() - retorna todos os elementos da coleção que satisfazem uma determinada condição.

IEnumerable -> Descreve um comoprtamento 
 
