/*

POO - é uma abordagem para desenvolvimento de software no qual a estrutura do software é baseada em objetos que interagem uns com os outros para
realizar uma tarefa.

Essa interação toma a forma de mensagens que são trocadas entre os objetos sendo que em resposta a uma mensagem um objeto pode executar uma ação ou método.

A solução contrária à POO é a Programação procedural, que consiste em executar o código de cima para baixo, sem classificar obrigatoriamente classes e métodos.
 
4 Pilares da POO ->

    Abstração: é uma forma de reduzir a complexidade e tornar o projeto e a implementação mais eficientes em sistemas complexos de software.
        
            Ex -> uma classe criada abstraindo componentes genéricos para evitar que muitos componentes tenham que ser criados nas instâncias

    Encapsulamento: é o processo no qual o acesso direto aos dados de um objeto não é permitido. Ele está ocultado e protegido.
        
            Ex -> usar private na criação de classes, componentes, strucs, etc.

    Herança: é um recurso que permite que você crie novas classes que reutilizam, estendem e modificam o comportamento definido em outras classes existentes.
        
            Ex -> uma classe base fornece uma funcionalidade comum (dados e comportamentos) e as classes derivadas herdam ou substituem essa funcionalidade.

    Polimorfismo (muitas formas): é a habilidade de objetos distintos responderem a mesma mensagem de sua própria maneira.
        
            Ex -> usar private na criação de classes, componentes, strucs, etc.
*/