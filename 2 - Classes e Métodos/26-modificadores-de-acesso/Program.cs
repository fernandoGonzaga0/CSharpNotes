/*
Os modificadores de acesso são palavras-chave usadas para especificar o acesso de um membro (campo, propriedade, método, etc) ou de um tipo (classe, struct, etc.).

Public -> poderá ser acessado por qualquer outro código no mesmo assembly ou em outro assembly que faz referência à ele.

Private -> pode ser acessado somente pelo código na mesma classe/struct.

Internal -> pode ser acessado por qualquer código no mesmo assembly, mas não de outro assembly.

Protected -> pode ser acessado por qualquer código no assemblu no qual ele é declarado ou de uma classe derivada de outro assembly.

File -> Restringe o escopo e a visibilidade de um tipo de nível superior ao arquivo no qual ele for declarado.
 
*/